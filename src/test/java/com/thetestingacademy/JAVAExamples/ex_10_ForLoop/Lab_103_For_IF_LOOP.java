package com.thetestingacademy.JAVAExamples.ex_10_ForLoop;

public class Lab_103_For_IF_LOOP {
    public static void main(String[] args) {
        for(int maneesha=0;maneesha<18;maneesha++)
        {
            if (maneesha>15)
            {
                System.out.println("There is a gift");
            }
            else {
                System.out.println("No Gift");
            }
        }
    }
}
